// Buffer'daki tüm değerlerin sıfırdan farklı olup olmadığını kontrol eden fonksiyon
bool gps_buffer_kontrol(uint8_t *buffer, size_t length) {
    for (size_t i = 0; i < length; i++) {
        if (buffer[i] == 0) {
            return false;
        }
    }
    return true;
}

